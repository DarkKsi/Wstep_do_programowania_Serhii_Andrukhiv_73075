print(type(1 + 2)) # 3 Dodawanie dwóch liczb całkowitych → wynik całkowity
print(type(1 + 4.5)) #5.5 Dodawanie liczby całkowitej i zmiennoprzecinkowej → wynik zmiennoprzecinkowy
print(type(3 / 2)) # 1.5 Dzielenie zwykłe zawsze zwraca liczbę zmiennoprzecinkową
print(type(4 / 2)) # 2.0 Wynik dzielenia jest zmiennoprzecinkowy
print(type(3 // 2)) # 1 Dzielenie całkowite – zwraca tylko część całkowitą
print(type(-3 // 2)) # -2 Dzielenie całkowite z zaokrągleniem w dół
print(type(11 % 2)) # 1 Reszta z dzielenia
print(type(2 ** 10)) # 1024 Potęgowanie
print(type(8 ** (1 / 3))) # 2.0 Pierwiastek sześcienny z 8 – wynik zmiennoprzecinkowy
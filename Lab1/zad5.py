a = float(input("Podaj długość pierwszego boku prostokąta: "))
b = float(input("Podaj długość drugiego boku prostokąta: "))

pole = a * b
obwod = 2 * (a + b)

print(f"Pole prostokąta wynosi: {pole}")
print(f"Obwód prostokąta wynosi: {obwod}")


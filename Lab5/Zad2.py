import math
import cmath

# a) √81
a = math.sqrt(81)

# b) 8^10
b = 8**10

# c) √2 + √3 + √6
c = math.sqrt(2) + math.sqrt(3) + math.sqrt(6)

# d) √-5
d = cmath.sqrt(-5)

# e) 3√125
e = round(125 ** (1/3))

print("a) √81 =", a)
print("b) 8^10 =", b)
print("c) √2 + √3 + √6 =", c)
print("d) √-5 =", d)
print("e) 3√125 =", e)

